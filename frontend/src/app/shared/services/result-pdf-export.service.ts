    doc.setTextColor(...REPORT_THEME.softPurple);
    doc.setFontSize(20);
    doc.text('Analysis Report', 14, 39);

    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9.5);
    doc.setTextColor(182, 176, 201);
